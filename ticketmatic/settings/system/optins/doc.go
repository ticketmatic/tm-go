// # Opt-in types
//
// There are 2 types:
//
// * Mandatory (40001)
//
// * Optional (40002)
//
// # Mandatory
//
// This type uses the fields yescaption and nocaption to present the opt-in to the
// user. The user will either need to accept or decline.
//
// # Options
//
// This type uses the field caption and by default the user accepts the opt-in.
// If the user wants to decline he will need to tick the checkbox.
//
// # Help Center
